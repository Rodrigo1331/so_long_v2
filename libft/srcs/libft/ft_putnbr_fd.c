#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long long int	n2;
	char			c;

	n2 = n;
	if (n2 < 0)
	{
		n2 = n2 * (-1);
		write(fd, "-", 1);
	}
	if (n2 >= 10)
	{
		ft_putnbr_fd(n2 / 10, fd);
		c = n2 % 10 + '0';
		write(fd, &c, 1);
	}
	if (n2 <= 9)
	{
		c = n2 % 10 + '0';
		write(fd, &c, 1);
	}
}
